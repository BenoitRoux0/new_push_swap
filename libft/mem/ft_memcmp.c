#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*m1;
	const unsigned char	*m2;
	size_t				i;

	if (!n)
		return (0);
	m1 = (const unsigned char *) s1;
	m2 = (const unsigned char *) s2;
	i = 0;
	while (i < n)
	{
		if (m1[i] != m2[i])
			return (m1[i] - m2[i]);
		i++;
	}
	return (0);
}
